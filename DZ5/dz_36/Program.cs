/*Задайте одномерный массив, заполненный случайными числами. Найдите сумму элементов, стоящих на нечётных позициях.

[3, 7, 23, 12] -> 19

[-4, -6, 89, 6] -> 0*/

int[] arr = new int[10]; // создаем массив из 10 элементов
Random rnd = new Random(); // создаем объект класса Random для генерации случайных чисел

// заполняем массив случайными числами
for (int i = 0; i < arr.Length; i++)
{
    arr[i] = rnd.Next(-100, 101); // генерируем случайное число от -100 до 100
}

int sum = 0;
// суммируем элементы, стоящие на нечётных позициях
for (int i = 1; i < arr.Length; i += 2)
{
    sum += arr[i];
}
Console.WriteLine("Массив: " + string.Join(", ", arr));
Console.WriteLine("Сумма элементов, стоящих на нечётных позициях: " + sum);

