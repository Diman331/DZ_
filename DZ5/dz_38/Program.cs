/* Задайте массив вещественных чисел. Найдите разницу между максимальным и минимальным элементов массива.

[3 7 22 2 78] -> 76*/

double[] arr = { 3, 7, 22, 2, 78 };
double min = arr[0]; // предполагаем, что первый элемент является минимальным
double max = arr[0]; // предполагаем, что первый элемент является максимальным

// проходим по всем элементам массива, чтобы найти минимальный и максимальный элементы
foreach (double element in arr)
{
    if (element < min)
    {
        min = element; // если текущий элемент меньше, чем текущий минимум, обновляем минимум
    }
    if (element > max)
    {
        max = element; // если текущий элемент больше, чем текущий максимум, обновляем максимум
    }
}

double diff = max - min;
Console.WriteLine("[" + string.Join(", ", arr) + "] -> " + diff);